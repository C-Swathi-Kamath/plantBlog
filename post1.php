            <h2 class="fw-bold fs-2 mb-5">Gardening is a Hobby for all!</h2>
            <p>Hobbies are a great distraction, aren't they? 
                As we grow from children to adults, hobbies have served as an escape from our daily routines and grab some fun. 
                One such hobby is gardening which isn't only fun but also educational. 
                Many people associate it as an activity for the old and believe that not everyone has a green thumb. 
                However, such general notions aren't true.
                 People across different ages can grow a garden and enjoy its physical and mental benefits.
                 </p>
            <img src="images/blog/p1.jpg" class="w-75 rounded mb-5" alt="Gardening tools spread out">
           </div>
                <h5 class="mb-3 fw-bolder">General benefits:</h5>
                <p class="mb-4">
                    Gardening is an exercise for the body and mind.
                     It includes digging, shoveling, cutting, etc., which involves muscle movements.
                      Also, Studies suggest people who garden are more likely to have a quality 7-hour sleep at night. 
                      Exercise can also help maintain body weight and reduce obesity. 
                </p>
                <p class="mb-4">
                    Gardening reduces stress levels and boosts self-esteem. 
                    Research suggests gardening helps cope with stress better than reading books. 
                    It also uplifts our mental health and keeps our minds in a positive state.
                </p>

                <h5 class="mb-3 fw-bolder">How does gardening help children?</h5>
                <p class="mb-4">
                    The best time to learn a new language is during childhood. 
                    Children grasp every bit of information as they are full of energy and curiosity to learn things. 
                    Gardening is also a language wherein plants address their requirements. 
                    It introduces children to noticing and understanding signals and act accordingly.
                     In a way, it develops necessary problem-solving skills in them. 
                     Gardening teaches the importance of patience, discipline, care, and empathy, contributing to their overall growth.
                </p>
                <p class="mb-4">
                    As kids grow into teenagers, they start experiencing stress from school and college. 
                    It's also a complicated phase in life where they explore in search of things they are naturally good at and enjoy. 
                    If they fail to do so, they may develop an identity crisis and be vulnerable to depression
                </p>
                <p class="mb-4">        
                    Research has shown that gardening helps tackle such problems. 
                    When their plants grow healthy and start blooming flowers and bearing fruits, it gives them a sense of achievement. 
                    They acknowledge their efforts which can boost their self-confidence.
                </p>

                <h5 class="mb-3 fw-bolder">What about adults?</h5>
                <p class="mb-4">
                    With responsibilities upon their shoulders, adults undergo stress and anxiety from work, family, and relationships. 
                    Gardening helps manage stress levels. 
                    Studies have suggested that greenery can reduce stress.
                     Nature has a calming effect on us. Having a garden or a few plants at home can bring us closer to nature. 
                </p>
                <p class="mb-4">        
                    Rapid urbanization has increased the levels of carbon pollutants in the air giving rise to several health problems.
                     It has also increased the pressure on agricultural lands for produce.
                      While planting edibles can help us become self-reliant, growing certain plants can also act as natural air purifiers providing clean air at all times. 
                      They are also aesthetically pleasing and enhance the beauty of our living spaces.
                </p>
                <p class="mb-4">        
                    With age, our brain starts to degrade, causing dementia. 
                    Alzheimer's disease (a form of dementia) is common in old age that makes people forgetful. 
                    Aged citizens involved in gardening activities have 36% lesser chances of suffering from Alzheimer's. 
                    Gardening serves as an effective treatment for dementia.
                     However, its onset is around the age of 30-40. 
                     Indulging in gardening activities can prevent dementia and keep your brain healthy
                </p>
                

                <h5 class="mb-3 fw-bolder">Final words</h5>
                <p class="mb-4">
                    Good sleep, good food, and good exercise contribute to our overall well-being and boosts our mood.
                     Gardening is an activity that benefits people of all ages. 
                     It also helps in community building and contributes to developing a clean and healthy environment. 
                     Yes, it is a bit of work, but have you ever got tired of doing something you love?
                </p>

