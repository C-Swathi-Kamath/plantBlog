            <div class="text-center">
            <h2 class="fw-bold fs-2 mb-5">What planter/pot should I buy?</h2>
            <p>Deciding the best kind of planter for your plants, your space and budget can be confusing.</p>
            <p>We've curated a list of differnt types of pots available along with the pros and cons to help you choose one that is suitable to your needs.</p>
            <img src="images/blog/p2.jpg" class="w-75 rounded mb-5" alt="plants in pots">
           </div>
            <ol>
                <li class="mb-3 fw-bolder">Plastic Pots - The regular Kind</li>
                <ul class="mb-5">
                <li class="mb-3">Pros: Cheap, Light weight, Durable, Multiple drainage holes for quick drainage</li>
                <li class="mb-3">Cons: Plastic! Plastic! Plastic!</li>
                <li class="mb-3">Grow: Veggies, Indoor plants and Flowers</li>
               </ul>

                <li class="mb-3 fw-bolder">Plastic Pots: Recycled Kind (Geofabric)</li>
                <ul class="mb-5">
                <li class="mb-3">Pros: Very cheap, Light weight, Durable, Fabric mixed with recycled plastic helps in drainage</li>
                <li class="mb-3">Cons: None, except that they are more suitable for outdoor spaces else use a drainage plate underneath for indoors.</li>
                <li class="mb-3">Grow: Any kind of veggies</li>
                 </ul>

                 <li class="mb-3 fw-bolder">Plastic Pots: HDPE kind</li>
                <ul class="mb-5">
                <li class="mb-3">Pros: Very cheap, Light weight, Durable, Holes for drainage</li>
                <li class="mb-3">Cons: Plastic! Plastic! Plastic! Not suitable for indoors</li>
                <li class="mb-3">Grow: Any kind of veggies</li>
                 </ul>

                 <li class="mb-3 fw-bolder">Terracota Pots</li>
                <ul class="mb-5">
                <li class="mb-3">Pros: Very cheap, Help plant sustain better in extreme hot and cold, Environment friendly</li>
                <li class="mb-3">Cons: Not very durable, Come in limited size options</li>
                <li class="mb-3">Grow: Small vegetables, succulents and ornamental plants. </li>
                 </ul>

                 <li class="mb-3 fw-bolder">Wooden Pots</li>
                 <ul class="mb-5">
                 <li class="mb-3">Pros: Look beautiful and can be customised to any size. Using plastic lining improves pot's life.</li>
                 <li class="mb-3">Cons: Expensive and intolerant to very humid conditions</li>
                 <li class="mb-3">Grow: Any kind of vegetables and flowering plants. </li>
                  </ul>
 
                  <li class="mb-3 fw-bolder">Metal Pots</li>
                 <ul class="mb-5">
                 <li class="mb-3">Pros: Aesthetically very pleasing (Try the ones with powder coating finish as they have longer durability against rusting)</li>
                 <li class="mb-3">Cons: Expensive and cant be used to grow plants directly without a second pot</li>
                 <li class="mb-3">Grow: Indoor plants and flowering plants</li>
                  </ul>

                  <li class="mb-3 fw-bolder">Ceramic pots</li>
                  <ul class="mb-5">
                  <li class="mb-3">Pros: Aesthetically pleasing (Come in a range of prices. Raw material used, finish and baking method determines the price in ceramic pots. Porcelain is the highest grade of ceramic available. It can be a one time investment that will stay with you for decades.)</li>
                  <li class="mb-3">Cons: Expensive</li>
                  <li class="mb-3">Grow: Indoor plants and flowering plants</li>
                   </ul>

                  </ol>
