            <h2 class="fw-bold fs-2 mb-5">Fix these Gardening Mistakes Today</h2>
            <p>As relaxing and fun gardening can be, there's always more to learn about it.
                 Whether you are a beginner or have been gardening for a few years now, mistakes are inevitable. 
                 But mistakes are a necessary part of the learning curve, Once we start identifying our mistakes, we can learn how to avoid them. 
                 With that being said, let's look into some common gardening errors that one can recognize and resolve in their plants.
                </p>
            <img src="images/blog/p3.jpg" class="w-75 rounded mb-5" alt="planting a plant">
           </div>
            <ol>
                <li class="mb-3 fw-bolder">Yellowing of Leaves</li>
                <p class="mb-4">
                    Plants need only a certain amount of water for functioning. 
                    The yellowing of leaves (chlorosis) is a sign that the plant is under stress.
                     While underwatering can turn leaves yellow, overwatering can also cause this problem. 
                     The key is to find the right amount of water that the plant requires. 
                     You can check it by touching the soil with your finger.
                     If the soil sticks to your finger, it doesn't require watering.
                </p>
                <p class="mb-4">
                    Now, depending upon the water requirements, there are three kinds of plants- Xerophytes, Mesophytes, and Hydrophytes. 
                    Succulents and cacti are xerophytes that have low water requirements. 
                    Hydrophytes are mostly aquatic plants that grow in water. 
                    The plants that we grow usually in our garden or indoors are Mesophytes. 
                    They need a moderate amount of water whenever the topsoil dries. 
                    The sticky-finger test is applicable only for Mesophytes. 
                </p>
                <p class="mb-4">
                    Insufficient light is another reason why leaves turn yellow.
                     While indoor plants have lower light requirements than outdoor ones, they still need to be kept in the sun for a few minutes once in about two weeks.
                      Before you buy the plants, check the availability of light in your house. 
                      Classify your space as bright, medium, or low-lit conditions and buy plants that can sustain in those environment. 
                      It is because certain plants may survive in medium-lit spaces but not in low-lit places.
                </p>
                <p class="mb-4">
                    Chlorosis also occurs in plants that lack nutrition. 
                    You can add fertilizer or compost to the soil about every 30 days. 
                    You can also use 20 ml of seaweed solution in 5 liters of water and pour it into the soil. 
                    Also, mix the top 1 inch of soil while you are at it to ensure the roots are well aerated.
                </p>

                <li class="mb-3 fw-bolder">Aphids infestations</li>
                <p class="mb-4">
                    Aphids are small insects that feed on the leaves and stems of the plants. 
                    You can take care of them using a simple shampoo or liquid soap solution.
                     Add 3-4 ml of liquid soap/shampoo in 1 liter of water. 
                     Mix it well and pump some air into it. 
                     Spray it on every corner of the plant. 
                     Make sure you do this during the evening.
                      In 2-3 hours, you will notice a reduction in the aphid infestation. 
                </p>

                <li class="mb-3 fw-bolder">Mealybugs</li>
                <p class="mb-4">
                    Have you noticed some powder-like molds on your plants? 
                    These are actually mealybugs. 
                    These insects are not only harmful to your plants but also attract ant infestation. 
                    You can get rid of them using a neem oil spray solution. 
                    Add 5-10 ml of neem oil and 2-3 ml of liquid soap in 300 ml of water and spray it over your plants. 
                    This method is very effective in removing soft-bodied insects such as mealybugs, lacy bugs, whiteflies, etc., and ants. 
                </p>

                <li class="mb-3 fw-bolder">Soil insects: </li>
                <p class="mb-4">
                    Now that we have taken care of the bugs on our plant, they are still at risk from the pathogens and insects in the soil. 
                    Mixing 100 grams of neem cake to the topsoil could solve this problem. 
                    Moreover, the cake also acts as an organic fertilizer that is rich in NPK (Nitrogen, Phosphorus, Potassium) nutrients beneficial to the plants. 
                </p>

                <li class="mb-3 fw-bolder">Stunted growth </li>
                <p class="mb-4">
                    Sometimes, you may not observe any growth in the new buds & leaves of your plants. 
                    What you do notice is limped, curled, and drooping leaves. 
                    These are signs of stunted growth in plants. 
                    It is related to stress again.
                     Having a scarcity or abundance of resources can negatively affect plants' growth. 
                     Check whether the plants have their basic necessities fulfilled and look for any infestation or infection in them.
                </p>
                <p class="mb-4">
                    If everything looks good, then you might have to prune a few parts of the plant. 
                    Pruning not only boosts plant growth but also helps them in utilizing the nutrients in an effective way. 
                    Spring is the best time to prune your plants. 
                    Give your plants a nice haircut and get rid of the unhealthy parts. 
                </p>
                
              </ol>
